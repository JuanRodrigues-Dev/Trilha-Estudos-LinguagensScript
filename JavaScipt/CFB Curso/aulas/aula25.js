/*
Arrow Functions - Funções de Seta em JavaScript
Arrow Functions são uma forma mais concisa de escrever funções em JavaScript. Elas são especialmente úteis para funções curtas e anônimas.
*/
const soma = (a, b) => {
    return a + b
}
somab = (a, b) => {
    return a + b
}
console.log(soma(5, 3)) //8
console.log(somab(10, 20)) //30